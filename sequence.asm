;TOGGLE BIT 0, THEN 1... AND SO ON
START:
		SETB P1.0
		CALL DELAY
		
		SETB P1.1
		CALL DELAY
		
		SETB P1.2
		CALL DELAY
		
		SETB P1.3
		CALL DELAY
		
		SETB P1.4
		CALL DELAY
		
		SETB P1.5
		CALL DELAY
		
		SETB P1.6
		CALL DELAY
		
		SETB P1.7
		CALL DELAY

		MOV P1, #00H
		CALL DELAY

		SJMP START

DELAY:
		MOV R2, #0AH
LOOP1:  MOV R1, #0C8H
LOOP0:  MOV R0, #0FFH
WAIT:   DJNZ R0, WAIT
	    DJNZ R1, LOOP0
		DJNZ R2, LOOP1
		RETI
		END
