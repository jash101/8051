START:
	   MOV P1, #055H
	   CALL DELAY
	   MOV P1, #0AAH
	   CALL DELAY
	   SJMP START
;GENERATE A DELAY OF APPROX 0.5 SECONDS 

;(0A*C8*FF = 10*200*255 = 510,000 * 1 MICROSECOND ~ 0.5 SECOND)

DELAY:

       MOV R2, #0AH

LOOP1: MOV R1, #0C8H

LOOP0: MOV R0, #0FFH

WAIT:  DJNZ R0, WAIT

       DJNZ R1, LOOP0

       DJNZ R2, LOOP1

       RETI

	   END
