;PROGRAM FOR STEPPER MOTOR PATTERN
START:	MOV		R1,		#100
		MOV		A,		#066H	;LOAD THE FORMAT
UP:		MOV		P1,		A		;SEND TO S.M
		ACALL	DELAY			;WAIT
		RR		A						;ROTATE RIGHT
		DJNZ	R1,		UP
		
		MOV		R1,		#100
		MOV		A,		#066H	;LOAD THE FORMAT
UP2:	MOV		P1,		A		;SEND TO S.M
		ACALL	DELAY			;WAIT
		RL		A						;ROTATE RIGHT
		DJNZ	R1,		UP2
		
DELAY:
		MOV TMOD, #10H;TIMER MODE SELECTION
	   MOV TL1, #0B0H ;LOAD TL COUNTER VALUE
	   MOV TH1, #3CH  ;LOAD TH COUNTER VALUE
	   SETB TR1		 ;START TIMER

HERE:
	   JNB TF1, HERE  ;WAIT TILL TF = 1 (OVERFLOW)
	   CLR TR1		;STOP TIMER
	   CLR TF1		;CLEAR TF1
	   RETI
	   END
